public class Kaomoji{
    public static String getKaomoji(String action){
        switch(action){
                case "Attack":
                return "(╯°□°）╯︵ ┻━┻";
                case "Flee":
                return "(ಥ﹏ಥ)";

                case "Talk":
                return "(｡♥‿♥｡)";

                default:
                return "(o_O)";
 



        }
    }
}